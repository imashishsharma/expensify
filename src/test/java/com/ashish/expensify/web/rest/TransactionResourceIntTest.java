    /**
     * Create an entity for this test.
     *
     * This is a static method, as tests for other entities might also need it,
     * if they test an entity which requires the current entity.
     */
    public static Transaction createEntity(EntityManager em) {
        Transaction transaction = new Transaction()
            .transactionName(DEFAULT_TRANSACTION_NAME)
            .transactionDate(DEFAULT_TRANSACTION_DATE)
            .createdDate(DEFAULT_CREATED_DATE)
            .modifiedDate(DEFAULT_MODIFIED_DATE)
            .delFlag(DEFAULT_DEL_FLAG)
            .comment(DEFAULT_COMMENT);
        // Add required entity
        User user = UserResourceIntTest.createEntity(em);
        em.persist(user);
        em.flush();
        transaction.setUser(user);
        // Add required entity
        TransactionCategory transactionCategory = TransactionCategoryResourceIntTest.createEntity(em);
        em.persist(transactionCategory);
        em.flush();
        transaction.setTransactionCategory(transactionCategory);
        return transaction;
    }

    @Before
    public void initTest() {
        transaction = createEntity(em);
    }

    @Test
    @Transactional
    public void createTransaction() throws Exception {
        int databaseSizeBeforeCreate = transactionRepository.findAll().size();

        // Create the Transaction
        TransactionDTO transactionDTO = transactionMapper.toDto(transaction);
        restTransactionMockMvc.perform(post("/api/transactions")
            .contentType(TestUtil.APPLICATION_JSON_UTF8)
            .content(TestUtil.convertObjectToJsonBytes(transactionDTO)))
            .andExpect(status().isCreated());

        // Validate the Transaction in the database
        List<Transaction> transactionList = transactionRepository.findAll();
        assertThat(transactionList).hasSize(databaseSizeBeforeCreate + 1);
        Transaction testTransaction = transactionList.get(transactionList.size() - 1);
        assertThat(testTransaction.getTransactionName()).isEqualTo(DEFAULT_TRANSACTION_NAME);
        assertThat(testTransaction.getTransactionDate()).isEqualTo(DEFAULT_TRANSACTION_DATE);
        assertThat(testTransaction.getCreatedDate()).isEqualTo(DEFAULT_CREATED_DATE);
        assertThat(testTransaction.getModifiedDate()).isEqualTo(DEFAULT_MODIFIED_DATE);
        assertThat(testTransaction.isDelFlag()).isEqualTo(DEFAULT_DEL_FLAG);
        assertThat(testTransaction.getComment()).isEqualTo(DEFAULT_COMMENT);
    }

